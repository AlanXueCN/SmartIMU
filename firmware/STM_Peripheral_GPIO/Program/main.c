/* Includes --------------------------------------------------------------------------------*/
#include "drivers\stm32f4_system.h"
#include "stm32f4xx_bsp.h"

/** @addtogroup STM32_Program
 *  @{
 */

/* Define ----------------------------------------------------------------------------------*/
/* Macro -----------------------------------------------------------------------------------*/
/* Typedef ---------------------------------------------------------------------------------*/
/* Variables -------------------------------------------------------------------------------*/
/* Prototypes ------------------------------------------------------------------------------*/
/* Functions -------------------------------------------------------------------------------*/

int main( void )
{
  HAL_Init();
  BSP_GPIO_Config();

  while (1) {
    LED_R_On();
    delay_ms(80);
    LED_R_Off();
    delay_ms(80);
    LED_G_On();
    delay_ms(80);
    LED_G_Off();
    delay_ms(80);
    LED_B_On();
    delay_ms(80);
    LED_B_Off();
    delay_ms(80);
  }
}

